class SimulatedExecutor:
    """
    Fast simulated executor using string similarity.

    Compares model patch to ground truth patch using diff similarity.
    Useful for rapid prototyping and prompt iteration.
    """

    def __init__(self, similarity_threshold: float = 0.7):
        """
        Args:
            similarity_threshold: Minimum similarity ratio to consider resolved
        """
        self.similarity_threshold = similarity_threshold

    def evaluate(self, model_patch: str, ground_truth_patch: str) -> bool:
        """
        Evaluate if model patch is similar enough to ground truth.

        Args:
            model_patch: Patch generated by model
            ground_truth_patch: Ground truth patch from dataset

        Returns:
            True if patches are similar enough (considered resolved)
        """
        if not model_patch or not ground_truth_patch:
            return False

        # Normalize whitespace
        model_lines = [line.strip() for line in model_patch.split('\n') if line.strip()]
        truth_lines = [line.strip() for line in ground_truth_patch.split('\n') if line.strip()]

        # Use SequenceMatcher for similarity
        matcher = difflib.SequenceMatcher(None, model_lines, truth_lines)
        similarity = matcher.ratio()

        return similarity >= self.similarity_threshold

    def execute(self, problem: Dict[str, Any], model_patch: str) -> Dict[str, Any]:
        """
        Execute simulated evaluation.

        Args:
            problem: Problem dictionary with ground truth
            model_patch: Model-generated patch

        Returns:
            Dictionary with evaluation results
        """
        ground_truth = problem.get('patch', '')

        resolved = self.evaluate(model_patch, ground_truth)

        return {
            'resolved': resolved,
            'execution_mode': 'simulated',
            'similarity_threshold': self.similarity_threshold
        }


