def get_executor(mode: str = 'simulated', **kwargs) -> Any:
    """
    Get executor instance.

    Args:
        mode: Execution mode ('simulated' or 'docker')
        **kwargs: Additional arguments passed to executor

    Returns:
        Executor instance
    """
    if mode == 'simulated':
        return SimulatedExecutor(**kwargs)
    elif mode == 'docker':
        return DockerExecutor(**kwargs)
    else:
        raise ValueError(f"Unknown execution mode: {mode}. Use 'simulated' or 'docker'")


